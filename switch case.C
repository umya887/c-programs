#include <stdio.h>

int main() {
    int start, end, i;

    printf("Enter the start number: ");
    scanf("%d", &start);

    printf("Enter the end number: ");
    scanf("%d", &end);

    for (i = start; i <= end; i++) {
        switch (i % 2) {
            case 0:
                printf("%d is Even\n", i);
                break;
            case 1:
                printf("%d is Odd\n", i);
                break;
        }
    }

    return 0;
}
